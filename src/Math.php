<?php

class Math
{
    public function add(int $x, int $y): int
    {
        return $x + $y;
    }

    public function sub(int $x, int $y): int
    {
        return $x - $y;
    }

    public function mul(int $x, int $y): int
    {
        return $x * $y;
    }

    public function div(int $x, int $y): int
    {
        return $x / $y;
    }
}
